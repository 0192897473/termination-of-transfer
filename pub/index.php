<h3 id="get_rights_back">Termination of transfers: get rights back!</h3>

Creators of all kinds routinely sign away rights to their work. Often, authors find that realizing creative or career goals can require giving some or all of their rights in copyright to publishers and other intermediaries. While many of these agreements last "for the life of copyright" (which in the United States means seventy years after the author dies!), the law takes into account that these terms can ultimately be unfair to authors and artists. This is where "terminations of transfers" come in.

Essentially, in the United States, authors can use the termination of transfers laws to regain the rights they have signed away. But because the law is complex, and because it requires that authors wait at least 35 years, creators need to be patient, savvy, and persistant to take full advantage of this power.

Many people use terminations because their works are tremendously successful, and they want to recapture rights to enjoy more of their work's financial success.

Many others find that the people who own their work are not acting as great stewards for it. The book might be out of print, the song might not be digitized or available online. For creators who want their work to be widely shared and enjoyed—who want to take advantage of a vibrant internet that might not have existed when they signed that contract all those years ago—terminations are a powerful option for getting their work back out in front of audiences.

Whatever <em>you</em> choose to do, the most improtant thing is that you know this law exists, and that you feel empowered to take advantage of it. Authors Alliance and Creative Commons are developing this tool to raise awareness of the law and its importance to creators who want to share their work widely. 

