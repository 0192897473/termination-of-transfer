<h3 id="what_does_it_do">What does this tool do?</h3>

<p>This tool is designed to raise awareness of the "termination of transfer" provisions and to make it easier for people to figure out whether the "termination rights" of U.S. copyright law might be relevant to their work. If a termination right might exist, the tool estimates - based on the information you provide - when the termination right might be excercised. The tool gathers this information along with some basic details about the work that might be useful and relevant to preparing the "termination notice" required to officially start the termination process.</p>
	
<p>This information can then be passed on to legal counsel who can then assist you in exercising your termination rights. Remember that this tool is provided for educational and awareness-raising purposes only—it cannot substitute for legal advice and analysis. Moreover, the tool is presently in "beta"—we are still ironing out the kinks and asking for your help to make it better!</p>

