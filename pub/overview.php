<h3 id="overview">Overview of the Tool</h3>

<p>This tool is designed to gather some initial information from you and to determine,
based on that information, whether you may be eligible to terminate an
<a class="glossarylink" href="glossary.php#agreement">agreement or transfer</a>
and if so, whether your <a class="glossarylink" href="glossary.php#notice_window">"notice window"</a> is currently open.</p>

<p>Below is an overview of the questions that are asked as you complete the tool
and an outline of the kinds of information you will need to have to hand in order
to complete the tool. While the tool does not record your responses from one session to the next, don't be afraid to run through it multiple times with different inputs. The tool is designed to provide helpful information even to users who do not have a full set of details, and it should take less than ten minutes to
complete any one approach.</p>

<p>If you are unsure about something the tool requires, try to answer it as best
and as accurately you can, giving as much information as you can so that this
information can be assessed by a lawyer who you consult with this information
or by a <a class="glossarylink" href="glossary.php#clinic">participating law clinic</a>.
We have tried to phrase the questions as straightforwardly as possible.
We have also provided links connected to some of the more complex terms or terms
with legal meanings, in an effort to help you better understand the nature of
the information the question is seeking and, thus, to enable you to hopefully
better answer the questions.</p>

<p>If you work through the tool and, based on the information you provide,
it seems that you are not currently eligible to terminate an agreement
in relation to a <a class="glossarylink" href="glossary.php#work">copyrighted work</a>,
do not be disheartened.  It may be that
you entered some of the information in incorrectly and/or that you need
some advice and assistance to correctly interpret and/or respond to the questions.</p>

<p>If you work through the tool and it says that you cannot currently
activate your termination right but that you may be able to do so in future,
feel free to try the tool again at that future date or to otherwise
investigate your termination options now or later.  Changes in your
circumstances over time can change what your termination options are so the
results of this tool will not hold true over time.  At any future date, you
should complete the tool again entering in the new and then current
information and/or seek new advice and assistance based on the then current information.</p>

<p>The tool is divided into the following sections &amp; these require the following pieces of information:</p>

