<h3>Additional Hints & Tips:</h3>
<p>
The US Library of Congress catalog provides an online searchable catalog (<a href="http://catalog.loc.gov/">http://catalog.loc.gov/</a>). It is the largest library in the world, with more than 130 million items on approximately 530 miles of bookshelves. The collections include more than 29 million books and other printed materials, 2.7 million recordings, 12 million photographs, 4.8 million maps, and 58 million manuscripts.  You may be able to access some of the Library of Congress' extensive collection if you are trying to locate a copy of a work, either onsite or via inter-library loan.<br>
<br>
When it comes to searching the Copyright Office's records, in addition to conducting the searches yourself, you also have the option of hiring private search firms or of paying the Copyright Office to conduct the search for you (<a href="http://www.copyright.gov/forms/search_estimate.html">http://www.copyright.gov/forms/search_estimate.html</a>).<br>
